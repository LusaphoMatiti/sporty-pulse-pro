// 16 base programs + 12 new equipment programs = 28 total
// Each session has exercises in the correct order for effective training:
// Compound movements first, isolation last

export type ExercisePlan = {
  exerciseName: string;
  order: number;
  beginnerSets: number;
  beginnerReps: number;
  intermediateSets: number;
  intermediateReps: number;
  advancedSets: number;
  advancedReps: number;
  restSeconds: number;
};

export type SessionPlan = {
  sessionNumber: number;
  focus: string;
  exercises: ExercisePlan[];
};

export type ProgramPlan = {
  name: string;
  description: string;
  muscleGroup: "UPPER" | "LOWER" | "CORE" | "FULLBODY";
  durationWeeks: number;
  sessionsPerWeek: number;
  tier: "FREE" | "EQUIPMENT";
  equipmentName: string | null;
  sessions: SessionPlan[];
};

// ─────────────────────────────────────────────
// HELPER — generates N sessions repeating the pattern
// ─────────────────────────────────────────────
function repeat(
  pattern: Omit<SessionPlan, "sessionNumber">[],
  totalSessions: number,
): SessionPlan[] {
  const sessions: SessionPlan[] = [];
  for (let i = 0; i < totalSessions; i++) {
    const template = pattern[i % pattern.length];
    sessions.push({ ...template, sessionNumber: i + 1 });
  }
  return sessions;
}

